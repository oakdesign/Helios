Helios_A10C = {}

Helios_A10C.Name = "A-10C"
Helios_A10C.FlamingCliffsAircraft = false

function Helios_A10C.HighImportance(mainPanelDevice)
	-- Send Altimeter Values	
	Helios_Udp.Send(2051, string.format("%0.4f;%0.4f;%0.5f", mainPanelDevice:get_argument_value(52), 
													  mainPanelDevice:get_argument_value(53), 
													  mainPanelDevice:get_argument_value(51)))
	-- Barometric Pressure Setting
	Helios_Udp.Send(2059, string.format("%0.2f;%0.2f;%0.2f;%0.3f", mainPanelDevice:get_argument_value(56), 
															mainPanelDevice:get_argument_value(57), 
															mainPanelDevice:get_argument_value(58), 
															mainPanelDevice:get_argument_value(59)))		
	-- Calcuate HSI Value
	Helios_Udp.Send(2029, string.format("%0.2f;%0.2f;%0.4f", mainPanelDevice:get_argument_value(29), 
													  mainPanelDevice:get_argument_value(30), 
													  mainPanelDevice:get_argument_value(31)))
	-- Calculate Total Fuel
	Helios_Udp.Send(2090, string.format("%0.2f;%0.2f;%0.5f", mainPanelDevice:get_argument_value(90), 
													  mainPanelDevice:get_argument_value(91), 
													  mainPanelDevice:get_argument_value(92)))

																								  
	Helios_Udp.Flush()
end

function Helios_A10C.LowImportance(mainPanelDevice)
	-- Get Radio Frequencies
	local lUHFRadio = GetDevice(54)
	Helios_Udp.Send(2000, string.format("%7.3f", lUHFRadio:get_frequency()/1000000))

	  -- TACAN Channel
	Helios_Udp.Send(2263, string.format("%0.2f;%0.2f;%0.2f", mainPanelDevice:get_argument_value(263), -- corrected for new TACAN dcs 1.5.6
													  mainPanelDevice:get_argument_value(264), 
													  mainPanelDevice:get_argument_value(265)))

	Helios_Udp.Flush()
end


